// Збережіть введене значення (використати метод prompt()) у змінну value
let value = prompt("Введіть число:");

// Вивести це значення у Console, використовуючи шаблонний рядок
console.log(`Введене значення: ${value}`);

// Перевірити: введене значення від'ємне? Додатнє? Дорівнює нулю?
// Перетворюємо рядок на число для перевірки
let numberValue = Number(value);

// Перевірка на валідність числа
if (isNaN(numberValue)) {
    console.log("Помилка: введено не число!");
    alert("Помилка: введено не число!");
} else {
    // Відповідь вивести за допомогою alert()
    if (numberValue > 0) {
        console.log("Число додатнє");
        alert("Число додатнє");
    } else if (numberValue < 0) {
        console.log("Число від'ємне");
        alert("Число від'ємне");
    } else {
        console.log("Число дорівнює нулю");
        alert("Число дорівнює нулю");
    }
}

